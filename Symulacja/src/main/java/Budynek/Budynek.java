package Budynek;

public abstract class Budynek {

    private int XBudynek;
    private int YBudynek;


    public int getXBudynek(){
        return this.XBudynek;
    }
    public void setXBudynek(int XBudynek)
    {
        this.XBudynek=XBudynek;
    }
    public int getYBudynek(){
        return this.YBudynek;
    }
    public void setYBudynek(int YBudynek)
    {
        this.YBudynek=YBudynek;
    }
}
